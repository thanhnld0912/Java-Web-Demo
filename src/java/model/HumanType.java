public class HumanType {
    private int ID;
    private String Name;
    public HumanType (int ID, String Name) {
        this.ID = ID;
        this.Name = Name;
    }
    public HumanType () {
        
    }
    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    
}
